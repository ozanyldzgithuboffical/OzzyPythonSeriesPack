# Boolean Variable Literal Examples
booleanTrue = True
booleanFalse = False
four = True + 3
two = False - 0
trueResult = (four == 4)
falseResult = (two == 3)
if __name__ == '__main__':
    print("Boolean True (True)", booleanTrue)
    print("Boolean False (False)", booleanFalse)
    print("Four:", four)
    print("Two:", two)
    print("True result:", trueResult)
    print("False result:", falseResult)
