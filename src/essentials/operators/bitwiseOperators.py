val1 = 4
val2 = 8
if __name__ == '__main__':
    # bitwise or
    print("Bitwise OR Operation:", val1 | val2)
    # bitwise and
    print("Bitwise AND Operation:", val1 & val2)
    # bitwise not
    print("Bitwise NOT Operation:", ~val1)
    # bitwise xor
    print("Bitwise XOR Operation:", val1 ^ val2)
    # bitwise right shift
    print("Bitwise Right Shift Operation:", val1 >> 5)
    # bitwise left shift
    print("Bitwise Left Shift Operation:", val1 << 5)
